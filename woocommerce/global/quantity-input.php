<div class="quantity">
	<div class="input-group">
          <span class="input-group-btn">
              <button type="button" class="minus btn btn-default btn-number"  data-type="minus" data-field="<?php echo esc_attr( $input_name ); ?>">

              </button>
          </span>
		<input type="text" name="<?php echo esc_attr( $input_name ); ?>" class="form-control input-number" value="<?php echo esc_attr( $input_value ); ?>" min="<?php echo esc_attr( $min_value ); ?>" <? if ($max_value != '' ) {?> max="<?php echo esc_attr( $max_value ); ?>"<? } else { ?>max="1000" <? } ?>>
		<span class="input-group-btn">
              <button type="button" class="plus btn btn-default btn-number" data-type="plus" data-field="<?php echo esc_attr( $input_name ); ?>">

              </button>
          </span>
